public class Solicitud {
    int numeroSolicitud;
    int CI;
    int costo;
    int presupuestoCompra;

    public Solicitud(int numeroSolicitud, int CI, int costo, int presupuestoCompra) {
        this.numeroSolicitud = numeroSolicitud;
        this.CI = CI;
        this.costo = costo;
        this.presupuestoCompra = presupuestoCompra;
    }
}
